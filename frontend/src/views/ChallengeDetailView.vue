<script setup lang="ts">
import { onMounted, ref, computed } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import { challengeService } from '../services/challenge'
import type { ChallengeDetails, ChallengeActivity, ChallengeLeaderboard } from '../types/challenge'

const route = useRoute()
const router = useRouter()

const loading = ref(true)
const challenge = ref<ChallengeDetails | null>(null)
const recentActivities = ref<ChallengeActivity[]>([])
const leaderboard = ref<ChallengeLeaderboard[]>([])

const goBack = () => {
  router.push('/dashboard')
}

// Calculate user's progress based on challenge data
const userProgress = computed(() => {
  if (!challenge.value) {
    return { completed: 0, percentage: 0, remaining: 0 }
  }

  // Find current user's participation data
  const currentUserParticipation = challenge.value.participants.find(p => p.isCurrentUser)
  if (!currentUserParticipation) {
    return { completed: 0, percentage: 0, remaining: 0 }
  }

  const completed = currentUserParticipation.currentTotal
  
  // Calculate goal based on challenge type (mock goals for now)
  let goal = 100 // Default goal
  switch (challenge.value.challengeType) {
    case 1: // Distance
      goal = 100
      break
    case 2: // Elevation
      goal = 10000
      break
    case 3: // Time
      goal = 20
      break
  }
  
  const percentage = Math.round((completed / goal) * 100)
  const remaining = Math.max(0, goal - completed)
  
  return { completed, percentage, remaining }
})

const getPositionColorClass = (position: number) => {
  switch (position) {
    case 1:
      return 'bg-yellow-500 text-yellow-900'
    case 2:
      return 'bg-gray-400 text-gray-900'
    case 3:
      return 'bg-orange-500 text-orange-900'
    default:
      return 'bg-gray-600 text-white'
  }
}

const formatTimeAgo = (dateString: string) => {
  const date = new Date(dateString)
  const now = new Date()
  const diffMs = now.getTime() - date.getTime()
  const diffHours = Math.floor(diffMs / (1000 * 60 * 60))
  const diffDays = Math.floor(diffHours / 24)
  
  if (diffDays > 0) {
    return `${diffDays} day${diffDays > 1 ? 's' : ''} ago`
  } else if (diffHours > 0) {
    return `${diffHours} hour${diffHours > 1 ? 's' : ''} ago`
  } else {
    return 'Just now'
  }
}

const formatDuration = (seconds: number) => {
  const hours = Math.floor(seconds / 3600)
  const minutes = Math.floor((seconds % 3600) / 60)
  
  if (hours > 0) {
    return `${hours}h ${minutes}m`
  } else {
    return `${minutes} min`
  }
}

const loadChallengeDetails = async () => {
  try {
    loading.value = true
    const challengeId = parseInt(route.params.id as string)
    
    // Load all the data in parallel
    const [challengeDetails, activities, leaderboardData] = await Promise.all([
      challengeService.getChallenge(challengeId),
      challengeService.getChallengeActivities(challengeId, 10),
      challengeService.getChallengeLeaderboard(challengeId)
    ])
    
    challenge.value = challengeDetails
    recentActivities.value = activities
    leaderboard.value = leaderboardData
    
  } catch (error) {
    console.error('Failed to load challenge details:', error)
    router.push('/dashboard')
  } finally {
    loading.value = false
  }
}

onMounted(() => {
  loadChallengeDetails()
})
</script>